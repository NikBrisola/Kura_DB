    CONSTRAINT PK_TIPO_EVENTO    PRIMARY KEY (ID_TIPO_EVENTO),
    CONSTRAINT UK_TIPO_EVENTO_CD UNIQUE (CD_TIPO),
    CONSTRAINT UK_TIPO_EVENTO_NM UNIQUE (NM_TIPO),
    CONSTRAINT CK_TIPO_ATIVO     CHECK (ST_ATIVO IN ('S','N'))
