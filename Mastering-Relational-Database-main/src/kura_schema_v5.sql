-- ----------------------------------------------------------------------------
-- VW_TIMELINE_PET — lida pelo Java TimelineService
-- Aliases V6: ID_AGENDAMENTO→ID_EVENTO, DT_AGENDAMENTO→DT_EVENTO
-- ----------------------------------------------------------------------------
CREATE OR REPLACE VIEW VW_TIMELINE_PET AS
SELECT
    a.ID_PET,
    a.ID_AGENDAMENTO  AS ID_EVENTO,
    p.NM_PET,
    a.DT_AGENDAMENTO  AS DT_EVENTO,
    a.DS_TIPO         AS DS_TIPO_EVENTO,
    a.ST_STATUS,
    a.ID_CLINICA,
    c.NM_CLINICA
FROM   AGENDAMENTO  a
JOIN   PET          p ON p.ID_PET     = a.ID_PET
JOIN   CLINICA      c ON c.ID_CLINICA = a.ID_CLINICA
WHERE  a.ID_PET IS NOT NULL;

COMMENT ON TABLE VW_TIMELINE_PET IS 'Linha do tempo de atendimentos por pet — lida pelo Java TimelineService.';

-- ----------------------------------------------------------------------------
-- VW_VACINAS_VENCENDO — alertas preventivos Luna (próximos 30 dias)
-- DS_TIPO AS NM_VACINA, DT_AGENDAMENTO AS DT_PROXIMA_DOSE (alias V6)
-- ----------------------------------------------------------------------------
CREATE OR REPLACE VIEW VW_VACINAS_VENCENDO AS
SELECT
    p.ID_PET,
    p.NM_PET,
    a.ID_TUTOR,
    a.DS_TIPO         AS NM_VACINA,
    a.DT_AGENDAMENTO  AS DT_PROXIMA_DOSE,
    a.ID_CLINICA,
    c.NM_CLINICA
FROM   AGENDAMENTO  a
JOIN   PET          p ON p.ID_PET     = a.ID_PET
JOIN   CLINICA      c ON c.ID_CLINICA = a.ID_CLINICA
WHERE  a.DS_TIPO   = 'VACINA'
  AND  a.ST_STATUS NOT IN ('CANCELADO','REALIZADO')
  AND  a.DT_AGENDAMENTO >= SYSTIMESTAMP
  AND  a.DT_AGENDAMENTO <= SYSTIMESTAMP + INTERVAL '30' DAY;

COMMENT ON TABLE VW_VACINAS_VENCENDO IS 'Vacinas agendadas nos próximos 30 dias — alimenta alertas preventivos da Luna.';

-- ----------------------------------------------------------------------------
-- VW_TIMELINE_CLINICA — eventos clínicos completos (.NET e Luna)
-- Fonte: EVENTO_CLINICO (escrita .NET) — separada da timeline AGENDAMENTO.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE VIEW VW_TIMELINE_CLINICA AS
SELECT
    p.ID_PET,
    p.NM_PET,
    e.ID_EVENTO,
    te.NM_TIPO         AS NM_TIPO_EVENTO,
    te.CD_TIPO         AS CD_TIPO_EVENTO,
    e.DT_EVENTO,
    v.NM_VETERINARIO,
    e.DS_OBSERVACAO,
    c.NM_CLINICA
FROM   PET           p
JOIN   EVENTO_CLINICO e  ON e.ID_PET         = p.ID_PET
JOIN   VETERINARIO    v  ON v.ID_VETERINARIO = e.ID_VETERINARIO
JOIN   TIPO_EVENTO   te  ON te.ID_TIPO_EVENTO = e.ID_TIPO_EVENTO
JOIN   CLINICA        c  ON c.ID_CLINICA      = e.ID_CLINICA
WHERE  p.ST_ATIVO = 'S';

COMMENT ON TABLE VW_TIMELINE_CLINICA IS 'Timeline clínica completa por pet (eventos EVENTO_CLINICO do .NET). Separada da VW_TIMELINE_PET.';

