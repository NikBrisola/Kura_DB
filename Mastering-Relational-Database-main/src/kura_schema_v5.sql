    CONSTRAINT CK_TUTOR_ATIVO    CHECK (ST_ATIVO IN ('S','N')),
    CONSTRAINT CK_TUTOR_AVISO    CHECK (ST_AVISO_PRIVACIDADE IN ('S','N'))
