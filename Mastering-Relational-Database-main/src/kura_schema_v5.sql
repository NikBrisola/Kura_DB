COMMENT ON TABLE  TIPO_EVENTO        IS 'Catálogo: CONSULTA, TELEORIENTACAO, VACINA, PRESCRICAO, EXAME, PROCEDIMENTO, RETORNO.';
COMMENT ON COLUMN TIPO_EVENTO.CD_TIPO IS 'Código imutável de negócio (usado pelo .NET EF Core como enum).';

